#include <unistd.h>

void	print(char *buf, char num)
{
	int	i;

	i = 0;
	while (buf[i] != num)
		i++;
	while (1)
	{
		write(1, &buf[i], 1);
		if (buf[i] == '\n')
			break ;
		i++;
	}
}
