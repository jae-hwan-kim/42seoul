#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	a;

	a = 1;
	while (a < ac)
	{
		i = 0;
		while (av[a][i] != '\0')
			i++;
		a++;
	}
	i = 0;
	a--;
	while (a >= 1)
	{
		while (av[a][i] != '\0')
		{
			write(1, &av[a][i], 1);
			i++;
		}
		write(1, "\n", 1);
		a--;
		i = 0;
	}
	return (0);
}
